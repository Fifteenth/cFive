package com.mn.zq.model;

public class BasicRelationship {
	
	String id;
	String code;
	String type;
	String basicRelationshipName;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getBasicRelationshipName() {
		return basicRelationshipName;
	}
	public void setBasicRelationshipName(String basicRelationshipName) {
		this.basicRelationshipName = basicRelationshipName;
	}

}
