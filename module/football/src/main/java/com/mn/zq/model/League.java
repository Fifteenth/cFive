package com.mn.zq.model;

public class League {

	private String season;
	private int round;
	private int number;
	private String club;
	private int win;
	private int draw;
	private int lose;
	private int goalEnter;
	private int goalLose;
	private int goalDifference;
	private int integration;
	private Integer cardRed;
	private Integer cardYellow;
	
	public String getSeason() {
		return season;
	}
	public void setSeason(String season) {
		this.season = season;
	}
	public int getRound() {
		return round;
	}
	public void setRound(int round) {
		this.round = round;
	}
	public int getNumber() {
		return number;
	}
	public void setNumber(int number) {
		this.number = number;
	}
	public String getClub() {
		return club;
	}
	public void setClub(String club) {
		this.club = club;
	}
	public int getWin() {
		return win;
	}
	public void setWin(int win) {
		this.win = win;
	}
	public int getDraw() {
		return draw;
	}
	public void setDraw(int draw) {
		this.draw = draw;
	}
	public int getLose() {
		return lose;
	}
	public void setLose(int lose) {
		this.lose = lose;
	}
	public int getGoalEnter() {
		return goalEnter;
	}
	public void setGoalEnter(int goalEnter) {
		this.goalEnter = goalEnter;
	}
	public int getGoalLose() {
		return goalLose;
	}
	public void setGoalLose(int goalLose) {
		this.goalLose = goalLose;
	}
	public int getGoalDifference() {
		return goalDifference;
	}
	public void setGoalDifference(int goalDifference) {
		this.goalDifference = goalDifference;
	}
	public int getIntegration() {
		return integration;
	}
	public void setIntegration(int integration) {
		this.integration = integration;
	}
	public Integer getCardRed() {
		return cardRed;
	}
	public void setCardRed(Integer cardRed) {
		this.cardRed = cardRed;
	}
	public Integer getCardYellow() {
		return cardYellow;
	}
	public void setCardYellow(Integer cardYellow) {
		this.cardYellow = cardYellow;
	}
	
	
}
